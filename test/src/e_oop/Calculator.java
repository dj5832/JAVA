package e_oop;

public class Calculator {

	// + - * / % 를 연산해주는 5개의 메서드를 만들어주세요.
	// 각각의 메서드는 2개의 파라미터를 받는다. 그 후 결과값 리턴
	
	
	double method1(double parameter1, double parameter2) {
		return parameter1 + parameter2;
	}
	double method2(double parameter1, double parameter2) {
		return parameter1 * parameter2;
	}
	double method3(double parameter1, double parameter2) {
		return parameter1 / parameter2;
	}
	double method4(double parameter1, double parameter2) {
		return parameter1 - parameter2;
	}
	double method5(double parameter1, double parameter2) {
		return parameter1 % parameter2;
	}
	
	
	
	
}
